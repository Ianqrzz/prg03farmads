    private void preencherCampos() {
        // Exibe os dados atuais da venda
        lblData.setText("Data: " + venda.getDataVenda().toString());
        txtValor.setText(String.valueOf(venda.getValorTotal()));
        
        // Exibe quem são o cliente e funcionário atuais
        lblClienteAtual.setText("Cliente: " + venda.getCliente().getNome());
        lblFuncionarioAtual.setText("Vendedor: " + venda.getFuncionario().getNome());
        
        // Preenche a tabela de itens da venda com o que já foi vendido
        atualizarTabelaItensVenda();
    }
    private void carregarDadosIniciais() {
    DefaultTableModel modelFunc = (DefaultTableModel) tblFuncionario.getModel();
    DefaultTableModel modelCli = (DefaultTableModel) tblCliente.getModel();
    DefaultTableModel modelProd = (DefaultTableModel) tblProdutos.getModel();
    
    modelFunc.setRowCount(0);
    modelCli.setRowCount(0);
    modelProd.setRowCount(0);
    
    // 1. Carrega Funcionários
    listaFuncionarios = funcionarioController.findAll();
    if (listaFuncionarios != null) {
        for (Funcionarios f : listaFuncionarios) {
            modelFunc.addRow(new Object[]{f.getNome(), f.getCargo()});
        }
    }
    
    // 2. Carrega Clientes
    listaClientes = clienteController.listAll(); 
    if (listaClientes != null) {
        for (Cliente c : listaClientes) {
            modelCli.addRow(new Object[]{c.getNome()});
        }
    }

    // 3. Carrega Produtos Disponíveis
    listaProdutosBanco = produtoController.findAll();
    if (listaProdutosBanco != null) {
        for (Produto p : listaProdutosBanco) {
            // Use getPrecoVenda para edição de venda
            modelProd.addRow(new Object[]{p.getNome(), p.getCusto()});
        }
    }
}
    private void atualizarTabelaItensVenda() {
    DefaultTableModel model = (DefaultTableModel) tblVendaMomento.getModel();
    model.setRowCount(0);
    // Venda contém uma lista de produtos
    for (Produto p : venda.getProdutos()) {
        model.addRow(new Object[]{p.getNome(), p.getCusto()});
    }
}

