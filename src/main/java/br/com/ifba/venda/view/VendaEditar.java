    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
 
    int linhaFunc = tblFuncionario.getSelectedRow();
    if (linhaFunc != -1) {
        venda.setFuncionario(listaFuncionarios.get(linhaFunc));
    }

  
    int linhaCli = tblCliente.getSelectedRow();
    if (linhaCli != -1) {
        venda.setCliente(listaClientes.get(linhaCli));
    }

   
    try {
        vendaController.save(venda);
        JOptionPane.showMessageDialog(this, "Venda atualizada com sucesso!");
        this.dispose();
    } catch (RuntimeException e) {
        JOptionPane.showMessageDialog(this, "Erro ao atualizar: " + e.getMessage(), 
                                      "Erro", JOptionPane.ERROR_MESSAGE);
    }      
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblProdutos.getSelectedRow();
    
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um produto na tabela primeiro!");
        return;
    }

 
    Produto produtoSelecionado = listaProdutosBanco.get(linhaSelecionada);


    produtosSelecionados.add(produtoSelecionado);
    valorTotalVenda += produtoSelecionado.getCusto();


    atualizarTabelaItensVenda();


    txtValor.setText(String.format("%.2f", valorTotalVenda));
    }//GEN-LAST:event_jButton1ActionPerformed

